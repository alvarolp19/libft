#include "libft.h"

char	*ft_strdup(char *s1)
{
	char	*dst;
	int		len;
	int		i;

	len = 0;
	i = 0;
	while (s1[len])
		len++;
	dst = (char *)malloc(sizeof(char) * (len + 1));
	if (!dst)
		return (0);
	while (i < len)
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
